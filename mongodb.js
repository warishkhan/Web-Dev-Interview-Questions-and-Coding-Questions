// What is MongoDB?

/*
MongoDB is an open-source NoSQL database management system 
that uses a document-oriented data model, 
making it flexible and scalable for storing and querying data.
*/


// How do you install MongoDB?

/*
To install MongoDB:

Download the appropriate MongoDB installer for 
your operating system from the official website.
Run the installer and follow the installation prompts.
After installation, start the MongoDB service.
Optionally, configure MongoDB to run as a service or daemon.
Verify the installation by running mongo in 
your terminal to access the MongoDB shell.
That's it! MongoDB is now installed on your system.
*/


// What is BSON (Binary JSON)?

/*
BSON (Binary JSON) is a binary-encoded serialization 
of JSON-like documents used by MongoDB to store and retrieve data. 
It extends the JSON model to provide additional data types and 
optimizations for efficient storage and traversal. 
BSON documents are lightweight, efficient, and self-descriptive, 
making them suitable for representing complex data structures 
and supporting MongoDB's features such as indexing, sharding, and replication.
*/


